/**
  @Function
    bool pick_change(int *part, ACTION_TYPE *type, int *done, int *reset_action)

  @Summary
    Brief one-line description of the function.
 */
bool drop_change(int *part, ACTION_TYPE *type, int *done, int *reset_action){
    if(((int)(*part)) == 0){
        if(type->servo_id == 3){
            ++(*part);
            return true;
        } else {}
    } else if(((int)(*part)) == 1){
        if(type->servo_id == 1){
            ++(*part);
            return true;
        } else {}
    } else if(((int)(*part)) == 2){
        if(type->servo_id == 2){
            *part = 0;
            *done = 0;            
            *reset_action = 0;
            
            Package done_pack;
            done_pack.DataReady = TRUE;
            PackageQueueSendToBack(done_pack,ReceiverQueue);   
            
            return true;
        } else {}
    } else {}
}


