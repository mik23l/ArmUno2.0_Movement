/**
  @Function
    void target_change(ARM_UPDATE *base_, ARM_UPDATE *right_, ARM_UPDATE *left_, ARM_UPDATE *grip_, ARM_UPDATE *target_) 

  @Summary
    Update the target value for certain servo.
 */
void target_change(ARM_UPDATE *base_, ARM_UPDATE *right_, ARM_UPDATE *left_, ARM_UPDATE *grip_, ARM_UPDATE *target_){
    switch(target_->servo_id)
    {
        case 0:
            base_->target_val = target_->target_val;
            break;
        case 1:
            right_->target_val = target_->target_val;
            break;
        case 2:
            left_->target_val = target_->target_val;
            break;
        case 3:
            grip_->target_val = target_->target_val;
            break;
        default:
            dbgOutputError(SYSTEM_STOP);
            break;
    }
}


